# This is my function tutorial.

def area(width, height):
    result = width * height
    return result

result = area(5, 6)
print("the area of 5 and 6 is:",result)

result = area(7,9)
print("the area of 7 and 9 is:",result)

result = area(4,8)
print("the area of 4 and 8 is:",result)

